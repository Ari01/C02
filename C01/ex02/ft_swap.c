#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

int main()
{
	int a = 4;
	int b = 2;
	ft_swap(&a, &b);
	printf("%d%d\n",a,b);

	return 0;
}
